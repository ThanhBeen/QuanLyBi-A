package Entyti;

/**
 *
 * @author thanh
 */
public class Ban {
    private int maBan;
    private String tenBan, loaiBan, trangThai;

    public Ban() {
    }

    public Ban(int maBan, String tenBan, String loaiBan, String trangThai) {
        this.maBan = maBan;
        this.tenBan = tenBan;
        this.loaiBan = loaiBan;
        this.trangThai = trangThai;
    }

    public int getMaBan() {
        return maBan;
    }

    public void setMaBan(int maBan) {
        this.maBan = maBan;
    }

    public String getTenBan() {
        return tenBan;
    }

    public void setTenBan(String tenBan) {
        this.tenBan = tenBan;
    }

    public String getLoaiBan() {
        return loaiBan;
    }

    public void setLoaiBan(String loaiBan) {
        this.loaiBan = loaiBan;
    }

    public String getTrangThai() {
        return trangThai;
    }

    public void setTrangThai(String trangThai) {
        this.trangThai = trangThai;
    }
    
}
